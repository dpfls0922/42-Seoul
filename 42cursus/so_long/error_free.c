#include "so_long.h"

void	error(char *s)
{
	int	i;

	i = 0;
	while (s[i])
	{
		write(1, &s[i], 1);
		i++;
	}
	exit(0);
}

void	ft_free_string(char *s)
{
	if (s)
	{
		free(s);
		s = 0;
	}
}
